
package com.uday.javaconcepts.controlflow;

public class ControlFlowExample {

    public static void main(String[] args) {

        int customerAgeValue = 20;

        if (customerAgeValue >= 18) {
            System.out.println("Customer is eligible to vote");
        } else {
            System.out.println("Customer is not eligible to vote");
        }
        // Output: Customer is eligible to vote
    }
}
